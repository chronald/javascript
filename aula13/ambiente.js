// Estrutura de repetição com teste lógico no inicio
while (c <= 5) {
    console.log(`Passo ${c}`)
    c++ 
} console.log('finish¹')

// Estrutura de repetição com teste lógico no final
var cc = 1
do {
    console.log(`Passo ${cc}`)
    cc++
} while (cc <= 6)
console.log('finish²')

